macas = int(input('Digite o número inteiro de maçãs vendidas: ')); 
bananas = int(input('Digite o número inteiro de bananas vendidas: ')); 

if bananas == macas:
    print('bananas e maçãs tiveram vendas iguais')
elif bananas > macas:
    print('bananas foram mais vendidas') 
else:
    print('maçãs foram mais vendidas')


